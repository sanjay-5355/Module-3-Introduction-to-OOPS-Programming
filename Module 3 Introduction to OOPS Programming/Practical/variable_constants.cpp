#include <iostream>
using namespace std;

int main() 

{
    // Declaring variables of different types
    int age = 20;
    float height = 5.9;
    char grade = 'A';
  
    // Declaring a constant
    const double PI = 3.14159;

    // Performing operations
    int nextYearAge = age + 1;
    float heightInCm = height * 30.48;

    // Output
    cout << "Age: " << age << "\n";
    cout << "Next year age: " << nextYearAge << "\n";
    cout << "Height in feet: " << height << "\n";
    cout << "Height in cm: " << heightInCm << " cm" << "\n";
    cout << "Grade: " << grade << "\n";
    cout << "Constant PI: " << PI << "\n";
    return 0;
}




